package ch12;

public class Myclass {

	private String name;
	private int age;

	public Myclass() {
		this.name = "名無し";
 		this.age = 0;
 	}
 	public String getName() {
 		return name;
 	}
 	public void setName(String name) {
 		this.name = name;
 	}
 	public int getAge() {
 		return age;
 	}
 	public void setAge(int age) {
 		this.age = age;
 	}
 }